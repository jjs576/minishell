#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	num;

	num = (long long int)n;
	if (num < 0)
	{
		num = -num;
		ft_putchar_fd('-', fd);
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
